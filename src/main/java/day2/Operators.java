package day2;

public class Operators {
    public static void main(String[] args) {
        int x=10;
        int y=2;
        int sum=x+y;
        System.out.println(sum);
        int sub=x-y;
        System.out.println(sub);
        int multi=x*y;
        System.out.println(multi);
        int div=x/y;
        System.out.println(div);
        int rem=x%y;
        System.out.println(rem);
    }
}
